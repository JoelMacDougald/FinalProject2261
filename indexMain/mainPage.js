// Define variables
let firstName = "John";
let lastName = "Doe";
let age = 30;

// Display a message using variables
console.log("Hello, my name is " + firstName + " " + lastName + " and I am " + age + " years old.");

// Perform arithmetic operations
let x = 10;
let y = 5;
let sum = x + y;
let difference = x - y;
let product = x * y;
let quotient = x / y;

// Display results of arithmetic operations
console.log("Sum: " + sum);
console.log("Difference: " + difference);
console.log("Product: " + product);
console.log("Quotient: " + quotient);

